package homework.day1.basetask;

public class TrainMethodsPrimitive {


    public void printInt() {
        int printInt = 6;
        System.out.println("я получил на вход число " + printInt);
    }

    public void printLong() {
        long printLong = 2147483647;
        System.out.println("я получил на вход  длинное число " + printLong);
    }

    public void printChar() {
        char printChar = 97;
        System.out.println("я получил на вход символ " + printChar);
    }

    public void printFloat() {
        float printFloat = 30.6f;
        System.out.println("я получил на вход дробное число " + printFloat);
    }

    public void printDouble() {
        double printDouble = 179769313486.231;
        System.out.println("я получил на вход длинное дробное число " + printDouble);
    }

    public void printShort() {
        short printShort = 32767;
        System.out.println("я получил на вход короткое число " + printShort);
    }

    public void printByte() {
        byte printByte = 127;
        System.out.println("я получил на вход очень короткое число " + printByte);
    }

    public void printBoolean() {
        boolean printBoolean = true;
        System.out.println("я получил на вход булево " + printBoolean);
    }


}
